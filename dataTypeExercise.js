// 1. String - Text data
let myString = "Hellow World";
let stringExample = "12345";
console.log("String:", myString);
console.log("String:", stringExample);

// 2. Number - Numeric data
let myNumber = 12345;
console.log("Number:", myNumber);

// 3. Boolean - True or False
let myBoolean = true;
console.log("Boolean:", myBoolean);

// 4. Undefined - Variable declared but not assigned
let myUndefined = undefined;
console.log("Undefined:", myUndefined);

// 5. Null - Intentional absence of value
let myNull = null;
console.log("Null:", myNull);

// 6. Symbol - Unique and immutable identifier
let mySymbol = Symbol("Fancy Identifier");
console.log("Symbol:", mySymbol);

// 7. BigInt - Numbers larger than the Number type can handle
let myBigInt = 12345678910n;
// console.log("BigInt:", myBigInt);

// 1. Object - Collection of key-value pairs
let myObject = { name: "John Doe", age: 100 };
console.log("Object:", myObject);

// 2. Array - Ordered list of values
let myArray = [100, 1000, 10000];
console.log("Array:", myArray);

